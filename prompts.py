def build_post_prompt(topic: str) -> str:
    return f"""
Напиши пост для Telegram на тему: {topic}

Требования:
- стиль: интересный, живой
- до 100 слов
- простой язык
- добавь вовлекающее начало
- можно использовать эмодзи (умеренно)
- в конце добавь вопрос для вовлечения

Сделай текст, который хочется дочитать до конца.
"""

